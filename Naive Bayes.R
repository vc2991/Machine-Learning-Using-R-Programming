# ROC-AUC Curve
library(ROCR)
ROCPred <- prediction(y_pred, test_set$Purchased) 
ROCPer <- performance(ROCPred, measure = "tpr", 
                      x.measure = "fpr")

auc <- performance(ROCPred, measure = "auc")
auc <- auc@y.values[[1]]
auc

# Plotting curve
